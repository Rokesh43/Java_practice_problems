// +=,-=,*=,/=,%=
class Compoundop 
{
	public static void main(String[] args) 
	{
		int a=10;
		a+=5;
		System.out.println("a:"+a);
		int b=30;
		b-=10;
		System.out.println("b:"+b);
		int c=30;
		c*=2;
		System.out.println("c:"+c);
		int d=50;
		d/=25;
		System.out.println("d:"+d);
		int e =60;
		e%=6;
	}
}
